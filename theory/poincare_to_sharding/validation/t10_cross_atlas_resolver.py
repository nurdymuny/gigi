from __future__ import annotations
from dataclasses import dataclass, field
from typing import Set, List, Tuple, Optional
import sys
import random


# ============================================================================
# Cross-atlas conflict type
# ============================================================================


@dataclass
class CrossAtlasConflict:
    """
    A write conflict in the cross-atlas setting.

    atlas_id: which atlas this conflict primarily belongs to (1 or 2).
              For bridge-overlap conflicts (visible to both), the
              `in_bridge` flag is True and `atlas_id` is the
              "primary" atlas (canonical choice).
    sign: +1 or -1; canceling partners have opposite signs.
    partner_id: id of the conflict that algebraically cancels this one.
                May be in the SAME atlas or the OTHER atlas.
    in_bridge: True if this conflict lives at a bridge identification
               (visible to both atlases). Informational only.
    """
    id: int
    atlas_id: int  # 1 or 2
    sign: int
    partner_id: int
    in_bridge: bool = False


def generate_cross_atlas_conflicts(
    n_intra_1_pairs: int,
    n_intra_2_pairs: int,
    n_cross_pairs: int,
    n_bridge_pairs: int = 0,
    seed: int = 0,
) -> List[CrossAtlasConflict]:
    """
    Generate a structured cross-atlas conflict graph.

    Args:
        n_intra_1_pairs: pairs where both partners are in atlas 1
        n_intra_2_pairs: pairs where both partners are in atlas 2
        n_cross_pairs: pairs where one partner is in atlas 1, one in 2
        n_bridge_pairs: pairs where BOTH partners are at bridge overlaps
                        (rare in practice; tests the edge case)
    """
    rng = random.Random(seed)
    conflicts: List[CrossAtlasConflict] = []
    next_id = 0

    def make_pair(atlas_a: int, atlas_b: int, in_bridge: bool):
        nonlocal next_id
        a_id, b_id = next_id, next_id + 1
        next_id += 2
        sign_a = +1 if rng.random() < 0.5 else -1
        conflicts.append(CrossAtlasConflict(
            id=a_id, atlas_id=atlas_a, sign=sign_a,
            partner_id=b_id, in_bridge=in_bridge,
        ))
        conflicts.append(CrossAtlasConflict(
            id=b_id, atlas_id=atlas_b, sign=-sign_a,
            partner_id=a_id, in_bridge=in_bridge,
        ))

    for _ in range(n_intra_1_pairs):
        make_pair(1, 1, in_bridge=False)
    for _ in range(n_intra_2_pairs):
        make_pair(2, 2, in_bridge=False)
    for _ in range(n_cross_pairs):
        make_pair(1, 2, in_bridge=False)
    for _ in range(n_bridge_pairs):
        # Bridge-pair: both partners tagged as in_bridge; atlases
        # assigned canonically (1, 2) but conceptually both are
        # visible to both atlases.
        make_pair(1, 2, in_bridge=True)

    rng.shuffle(conflicts)
    return conflicts


# ============================================================================
# Cross-atlas resolver (Clean Finger Move extended)
# ============================================================================


@dataclass
class CrossAtlasResolverTrace:
    initial_count: int
    steps: int
    residual_size: int
    monotonic_decrease_violations: int
    cross_atlas_resolutions: int
    intra_atlas_resolutions: int
    bridge_resolutions: int
    blocked: bool


def resolve_cross_atlas_finger_move(
    conflicts: List[CrossAtlasConflict],
    search_seed: int = 0,
) -> CrossAtlasResolverTrace:
    """
    Cross-atlas Clean Finger Move resolver.

    Algorithm is ATLAS-AGNOSTIC: at each step, find any canceling pair
    and remove it. The atlas_id and in_bridge fields are tracked for
    reporting but don't influence the resolution order.

    By the T10 claim, the H_2 = 0 precondition (every conflict has a
    canceling partner) is sufficient for clean termination regardless
    of cross-atlas distribution.
    """
    rng = random.Random(search_seed)
    by_id = {c.id: c for c in conflicts}
    unresolved: Set[int] = set(by_id.keys())
    initial = len(unresolved)

    # Pre-validate: every conflict has a canceling partner with opposite sign
    for c in conflicts:
        if c.partner_id not in by_id:
            raise ValueError(f"conflict {c.id} has missing partner {c.partner_id}")
        if c.sign + by_id[c.partner_id].sign != 0:
            raise ValueError(f"conflicts {c.id}, {c.partner_id} have same sign")

    steps = 0
    mono_violations = 0
    cross_resolutions = 0
    intra_resolutions = 0
    bridge_resolutions = 0
    last_size = initial

    while unresolved:
        # Find any canceling pair (randomize search order to verify
        # ordering invariance)
        candidates = list(unresolved)
        rng.shuffle(candidates)
        pair = None
        for a_id in candidates:
            a = by_id[a_id]
            if a.partner_id in unresolved and a.sign + by_id[a.partner_id].sign == 0:
                pair = (a_id, a.partner_id)
                break

        if pair is None:
            return CrossAtlasResolverTrace(
                initial_count=initial, steps=steps,
                residual_size=len(unresolved),
                monotonic_decrease_violations=mono_violations,
                cross_atlas_resolutions=cross_resolutions,
                intra_atlas_resolutions=intra_resolutions,
                bridge_resolutions=bridge_resolutions,
                blocked=True,
            )

        a_id, b_id = pair
        a, b = by_id[a_id], by_id[b_id]

        # Classify the resolution for reporting
        if a.in_bridge or b.in_bridge:
            bridge_resolutions += 1
        elif a.atlas_id != b.atlas_id:
            cross_resolutions += 1
        else:
            intra_resolutions += 1

        unresolved.discard(a_id)
        unresolved.discard(b_id)
        new_size = len(unresolved)
        if new_size != last_size - 2:
            mono_violations += 1
        last_size = new_size
        steps += 1

    return CrossAtlasResolverTrace(
        initial_count=initial, steps=steps,
        residual_size=0,
        monotonic_decrease_violations=mono_violations,
        cross_atlas_resolutions=cross_resolutions,
        intra_atlas_resolutions=intra_resolutions,
        bridge_resolutions=bridge_resolutions,
        blocked=False,
    )


def all_clean(trace: CrossAtlasResolverTrace, expected_steps: int) -> bool:
    return (
        not trace.blocked
        and trace.residual_size == 0
        and trace.steps == expected_steps
        and trace.monotonic_decrease_violations == 0
    )


