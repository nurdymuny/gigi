def run_part_a():
    """Part (A): all-cross-atlas balanced distribution."""
    print("\n-- Part (A): ALL-CROSS-ATLAS PAIRS " + "-" * 32)
    results = []
    for n_pairs in [10, 25, 50]:
        conflicts = generate_cross_atlas_conflicts(
            n_intra_1_pairs=0, n_intra_2_pairs=0,
            n_cross_pairs=n_pairs, n_bridge_pairs=0,
            seed=42,
        )
        trace = resolve_cross_atlas_finger_move(conflicts, search_seed=7)
        ok = all_clean(trace, n_pairs)
        results.append((n_pairs, trace, ok))
        flag = "PASS" if ok else "FAIL"
        print(f"  {n_pairs} cross-atlas pairs: steps={trace.steps}, "
              f"residual={trace.residual_size}, cross_resolutions={trace.cross_atlas_resolutions}, "
              f"mono_viol={trace.monotonic_decrease_violations} [{flag}]")
    return all(ok for (_, _, ok) in results)


def run_part_b():
    """Part (B): mixed distribution."""
    print("\n-- Part (B): MIXED DISTRIBUTION " + "-" * 35)
    results = []
    for (n1, n2, ncr) in [(5, 5, 5), (10, 5, 10), (15, 15, 15), (20, 10, 30)]:
        conflicts = generate_cross_atlas_conflicts(
            n_intra_1_pairs=n1, n_intra_2_pairs=n2,
            n_cross_pairs=ncr, n_bridge_pairs=0,
            seed=42,
        )
        n_total_pairs = n1 + n2 + ncr
        trace = resolve_cross_atlas_finger_move(conflicts, search_seed=7)
        ok = all_clean(trace, n_total_pairs)
        results.append((n1, n2, ncr, trace, ok))
        flag = "PASS" if ok else "FAIL"
        print(f"  intra1={n1}, intra2={n2}, cross={ncr}: steps={trace.steps}, "
              f"intra={trace.intra_atlas_resolutions}, cross={trace.cross_atlas_resolutions}, "
              f"residual={trace.residual_size}, mono_viol={trace.monotonic_decrease_violations} [{flag}]")
    return all(ok for (_, _, _, _, ok) in results)


